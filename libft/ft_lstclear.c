#include "libft.h"

void	ft_lstclear(t_node **lst, void (*del)(void*))
{
	t_node *tmp;

	if (!lst)
		return ;
	while (*lst)
	{
		tmp = (*lst)->next;
		if (del)
			del((*lst)->content);
		free(*lst);
		*lst = tmp;
	}
}